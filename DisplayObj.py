def displayObject(obj,x,y):
    from gfxhat import lcd
    while(x < 0 or y < 0 or x > 127 or y > 63):
        if(x < 0):
            x = 128+x
        if(y < 0):
            y = 64+y
        if(x > 127):
            x = x-128
        if(y > 63):
            y = y-63
    theX = x
    theY = y
    for lines in obj:
        for pixs in lines:
            if(pixs == 1):
                lcd.set_pixel(theX,theY,1)
                lcd.show()
            theX += 1
            if(theX > 127):
                theX = 0
        theY += 1
        if(theY > 63):
            theY = 0
        theX = x

